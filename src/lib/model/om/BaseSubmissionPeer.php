	/**
	 * Selects a collection of Submission objects pre-filled with their Assignment objects.
	 * @param      Criteria  $c
	 * @param      PropelPDO $con
	 * @param      String    $join_behavior the type of joins to use, defaults to Criteria::LEFT_JOIN
	 * @return     array Array of Submission objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinAssignment(Criteria $c, $con = null, $join_behavior = Criteria::LEFT_JOIN)
	{
		$c = clone $c;

		// Set the correct dbName if it has not been overridden
		if ($c->getDbName() == Propel::getDefaultDB()) {
			$c->setDbName(self::DATABASE_NAME);
		}

		SubmissionPeer::addSelectColumns($c);
		$startcol = (SubmissionPeer::NUM_COLUMNS - SubmissionPeer::NUM_LAZY_LOAD_COLUMNS);
		AssignmentPeer::addSelectColumns($c);

		$c->addJoin(array(SubmissionPeer::ASSIGNMENT_ID,), array(AssignmentPeer::ID,), $join_behavior);
		$stmt = BasePeer::doSelect($c, $con);
		$results = array();

		while ($row = $stmt->fetch(PDO::FETCH_NUM)) {
			$key1 = SubmissionPeer::getPrimaryKeyHashFromRow($row, 0);
			if (null !== ($obj1 = SubmissionPeer::getInstanceFromPool($key1))) {
				// We no longer rehydrate the object, since this can cause data loss.
				// See http://propel.phpdb.org/trac/ticket/509
				// $obj1->hydrate($row, 0, true); // rehydrate
			} else {

				$omClass = SubmissionPeer::getOMClass();

				$cls = substr('.'.$omClass, strrpos('.'.$omClass, '.') + 1);
				$obj1 = new $cls();
				$obj1->hydrate($row);
				SubmissionPeer::addInstanceToPool($obj1, $key1);
			} // if $obj1 already loaded

			$key2 = AssignmentPeer::getPrimaryKeyHashFromRow($row, $startcol);
			if ($key2 !== null) {
				$obj2 = AssignmentPeer::getInstanceFromPool($key2);
				if (!$obj2) {

					$omClass = AssignmentPeer::getOMClass();

					$cls = substr('.'.$omClass, strrpos('.'.$omClass, '.') + 1);
					$obj2 = new $cls();
					$obj2->hydrate($row, $startcol);
					AssignmentPeer::addInstanceToPool($obj2, $key2);
				} // if obj2 already loaded

				// Add the $obj1 (Submission) to $obj2 (Assignment)
				$obj2->addSubmission($obj1);

			} // if joined row was not null

			$results[] = $obj1;
		}
		$stmt->closeCursor();
		return $results;
	}


